const addtion = (x, y)=>{
  return Number(x) + Number(y);
}
const subtraction = (x, y) => {
  return x - y;
}
const division = (x, y) => {
  return x / y;
}
const multiplication = (x, y) => {
  return x*y;
}

export default {
  addtion: addtion,
  subtraction: subtraction,
  division: division,
  multiplication: multiplication
}